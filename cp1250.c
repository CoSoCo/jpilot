/****************************** Main Code *************************************/
void Win2Lat(char *const buf, int buf_len)
{
   unsigned char *p;
   int i;

   if (buf == NULL) return;

   for (i=0, p = (unsigned char *)buf; *p && i < buf_len; p++, i++) {
      if (isCZ(*p)) {
         *p = w2l[(*p) & 0x7f];
      }
   }
}

void Lat2Win(char *const buf, int buf_len)
{
   unsigned char *p;
   int i;

   if (buf == NULL) return;

   for (i=0, p = (unsigned char *)buf; *p && i < buf_len; p++, i++) {
      if (isCZ(*p)) {
         *p = l2w[(*p) & 0x7f];
      }
   }
}

